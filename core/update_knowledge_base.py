def update_skill_file(filename, content):
    filepath = os.path.join(SKILL_DIR, filename)
    if not os.path.exists(filepath):
        print(f"文件不存在: {filename}")
        return False
    
    with open(filepath, 'a', encoding='utf-8') as f:
        f.write(content)
