def save_novel(novel_data):
    os.makedirs('output', exist_ok=True)
    
    # 保存小说设定
    setting_file = 'output/长夜将明_小说设定.txt'
    with open(setting_file, 'w', encoding='utf-8') as f:
        f.write(f"{'='*60}\n")
        f.write(f"《{novel_data['name']}》\n")
        f.write(f"{novel_data['tagline']}\n")
        f.write(f"{'='*60}\n\n")
        
        for key, value in novel_data.items():
            if key not in ['name', 'tagline']:
                f.write(f"\n{'='*60}\n")
                f.write(f"{key.upper()}\n")
                f.write(f"{'='*60}\n\n")
                f.write(str(value))
    
    print(f"✅ 设定已保存: {setting_file}")
    return setting_file

def save_chapter(chapter_num, title, content):
    filename = f"第{chapter_num:02d}章_{title}.txt"
    
    os.makedirs('output', exist_ok=True)
    filepath = os.path.join('output', filename)
    
    header = f"""{'='*60}
《长夜将明》
第{chapter_num}章：{title}
{'='*60}

"""
    
    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(header)
        f.write(content)
    
    print(f"✅ 章节已保存: {filepath}")
    return filepath

def save_full_novel(all_chapters):
    filename = "长夜将明_前10章.txt"
    
    os.makedirs('output', exist_ok=True)
    filepath = os.path.join('output', filename)
    
    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(f"{'='*60}\n")
        f.write("《长夜将明》\n")
        f.write("前10章完整版\n")
        f.write(f"{'='*60}\n\n")
        
        for chapter in all_chapters:
            f.write(f"\n\n{'='*60}\n")
            f.write(f"第{chapter['num']:02d}章：{chapter['title']}\n")
            f.write(f"{'='*60}\n\n")
            f.write(chapter['content'])
    
    print(f"✅ 完整版已保存: {filepath}")
    return filepath

