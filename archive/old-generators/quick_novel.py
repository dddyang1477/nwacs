def save_novel(novel):
    save_path = f'manuscripts/{novel["title"]}.txt'
    os.makedirs('manuscripts', exist_ok=True)
    
    with open(save_path, 'w', encoding='utf-8') as f:
        f.write(f'《{novel["title"]}》\n\n')
        f.write(f'主题: {novel["theme"]}\n')
        f.write(f'章节: {novel["chapters_count"]} 章\n')
        f.write(f'字数: {novel["word_count"]} 字\n\n')
        f.write('=' * 60 + '\n\n')
        
        for chapter in novel['chapters']:
            f.write(f'【{chapter["title"]}】\n')
            f.write(chapter['content'])
            f.write('\n\n' + '-' * 60 + '\n\n')
    
    return save_path

