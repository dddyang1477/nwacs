def main():
    print("\n" + "=" * 80)
    print("          正在生成8个创新剧情技能")
    print("=" * 80)
    
    output_dir = 'output/创新技能'
    os.makedirs(output_dir, exist_ok=True)
    
    generated = []
    for skill in skills:
        safe_name = ''.join(c for c in skill['name'] if c not in '\\/:*?"<>|')
        filepath = os.path.join(output_dir, f"{safe_name}.txt")
        
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(skill['content'])
        
        generated.append((skill, filepath))
        print(f"  ✓ 已生成: {skill['name']}")
    
    print("\n" + "=" * 80)
    print("                    生成完成！")
    print("=" * 80)
    print(f"\n📚 已成功生成 {len(generated)} 个创新技能：\n")
    for skill, filepath in generated:
        print(f"【{skill['name']}】")
        print(f"  描述: {skill['description']}")
        print(f"  路径: {filepath}\n")
    
    print("=" * 80)
    print("\n所有技能已保存到 output/创新技能/ 目录！")

if __name__ == "__main__":
    main()
