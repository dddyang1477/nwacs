def main():
    print("=" * 60)
    print("  NWACS 影视感引擎 - 《黑日藏锋》逐章生成")
    print("=" * 60)
    
    from cinematic_writing_engine import get_cinematic_engine
    cine = get_cinematic_engine()
    print("✅ 影视感引擎已加载\n")
    
    total_chars = 0
    for ch_num, ch_title, ch_summary, ch_points in CHAPTERS:
        print(f"[第{ch_num}章] {ch_title} - 生成中...", end=" ", flush=True)
        t0 = time.time()
        
        try:
            content = cine.generate_cinematic_chapter(
                ch_num, ch_title, ch_summary, ch_points, "玄幻", 3000
            )
            t1 = time.time()
            
            if content:
                outfile = os.path.join(OUTPUT_DIR, f"黑日藏锋_第{ch_num}章.txt")
                with open(outfile, "w", encoding="utf-8") as f:
                    f.write(f"第{ch_num}章 {ch_title}\n\n{content}")
                print(f"✅ {len(content)}字 ({t1-t0:.0f}s)", flush=True)
                total_chars += len(content)
            else:
                print(f"❌ 返回空 ({t1-t0:.0f}s)", flush=True)
        except Exception as e:
            print(f"❌ {e}", flush=True)
        
        time.sleep(1)
    
    print(f"\n{'='*60}")
    print(f"  完成! 共{total_chars}字符, 保存至: {OUTPUT_DIR}")
    print(f"{'='*60}")

if __name__ == "__main__":
    main()
